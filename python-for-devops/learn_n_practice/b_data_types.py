'''
Python supports 3 categories of data types:
    - Basic Types: [Integer, Float, Complex, Boolean, String]
    - Container Types: [List, Tuples, Sets, Dictionary]
    - User- defined Types: [Class]
'''

# [Basic Type]
# Integer
print(4)
print(1e308)  #10 to the power 308, Python supports really big numbers


# Float
print(6.9)
print(1.7e308)

# Boolean
print(True)
print(False)

# Complex
print(6+9j)


# String - This can be written using multiple mays
print('Learn Python') # Single inverted comma
print("Learn Python") # Double inverted comma
print('''Learn Python''') # Triple Single inverted comma
print("""Learn Python""") #Triple Double Inverted comma


# [Container Types]- will practice in details

# List
print([1,2,3,4,5])

# Tuple
print((1,2,3,4,5))

# Set
print({1,2,3,4,5})

# Dictionary - {Key:Value}
print({"name": "Ravindar", "Age": 69})

