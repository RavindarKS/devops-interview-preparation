# To display anything on screen we use 'print' function
print('This String will be displayed on Screen')

# Print function accepts multiple arguments
print("These are fruit name")
print("Apple","Bananna","Cherry",)

# If you execute the code you'll observe that after displaying "These are fruit name", Next print statement starts from new line. 


# Now these can be modified by passing values to 'sep' and 'end' parameters of print function.
print("These are fruit name", end=" ")
print("Apple","Bananna","Cherry", sep="=")


